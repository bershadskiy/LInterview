package entity;

abstract class PersonAbstract extends ReporterAbstract {
    public byte getAge() {
        return age;
    }

    public void setAge(byte age) {
        this.age = age;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    private byte age;
    private String address;
}
